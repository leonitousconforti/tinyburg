/** Loads a template to be used by the template matching image operations. */
export const loadTemplateByName = async (name: SpriteTemplateName | CharacterTemplateName): Promise<Image> => {
    // How to get to the templates directory from here
    const baseUrl = new URL("../../templates/", import.meta.url);

    // Load the raw and metadata files
    const pixels = await fs.readFile(new URL(`raw/${name}.bin`, baseUrl));
    const metadata = await fs.readFile(new URL(`metadata/${name}.json`, baseUrl), { encoding: "ascii" });
    const metadataJson = JSON.parse(metadata) as sharp.Metadata;

    // I am very confident these will exist for every metadata
    // file, but if they don't just throw an error
    assert(metadataJson.width, `Invalid metadata file for ${name}, missing property 'width'`);
    assert(metadataJson.height, `Invalid metadata file for ${name}, missing property 'height'`);
    assert(metadataJson.channels, `Invalid metadata file for ${name}, missing property 'channels'`);

    // Create a new image
    return {
        width: metadataJson.width,
        height: metadataJson.height,
        channels: metadataJson.channels,
        format: imageTypeFromChannelsForPng(metadataJson.channels),
        pixels,
    };
};

/**
 * Loads multiple templates to be used by the template matching image
 * operations.
 */
export async function loadTemplatesByNames<
    T extends Array<SpriteTemplateName | CharacterTemplateName>,
    U extends { [C in T[number]]: Image },
>(...names: T): Promise<U> {
    // Maps every name from the parameters to its entry in an object.
    const charPromises = names.map(async (name) => [name, await loadTemplateByName(name)] as [T[number], Image]);
    const charsAwaited = await Promise.all(charPromises);

    // Create an object from all the entries
    return Object.fromEntries(charsAwaited) as U;
}

/** Loads char templates to be used by the ocr image operations. */
export async function loadCharTemplates<T extends Characters[], U extends { [C in T[number]]: Image }>(
    ...chars: T
): Promise<U> {
    // Transforms a character into an array [character, characterTemplateImage]
    const charTransformer = async (char: T[number]): Promise<[T[number], Image]> =>
        [char, await loadTemplateByName(`char_${char.codePointAt(0)}` as CharacterTemplateName)] as [T[number], Image];

    // Maps every char from the parameters to its entry in an object. Since the
    // char transformer is asynchronous the map operation returns an array of promises
    const charPromises = chars.map((char) => charTransformer(char));
    const charsAwaited = await Promise.all(charPromises);

    // Create an object from all the entries
    return Object.fromEntries(charsAwaited) as U;
}

